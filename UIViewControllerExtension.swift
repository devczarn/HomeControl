import Foundation
import UIKit

extension UIViewController {
    func customizeNavigation(){
        navigationItem.setHidesBackButton(true, animated: true)
    }
    
    func backButtonWhite(){
        navigationItem.setHidesBackButton(true, animated: true)
    }
    
    
}
